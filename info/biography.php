<h1>Biografía</h1>

<h2>Ascendencia y primeros años</h2>

<p>
Moctezuma era el bisnieto de

Moctezuma I a partir de su hija

Atotoztli II y el esposo de ésta Huehue Tezozómoc (no confundir con el

líder tepaneca). Según algunas fuentes, Tezozómoc era el hijo de

Itzcóatl, lo cual haría a Moctezuma el bisnieto de éste también. Otras fuentes sin embargo describen que Tezozómoc era un líder de

Ecatepec, sobrino de Itzcóatl e hijo de

Chimalpopoca, lo cual haría a Moctezuma el bisnieto de Chimalpopoca.​ Su padre fue

Axayácatl, el sexto

tlatoani de Tenochtitlán, y su madre fue una de las esposas de éste, Izelcoatzin o Xochicueyetl, ambas hijas de

Nezahualcóyotl.​​ Dos de sus tíos fueron

Tízoc y

Ahuízotl, los dos gobernantes anteriores.​

</p>

<image-element float="right" src="/res/retrato-de-moctezuma.png" alt="Retrato de Moctezuma"></image-element>

<p>
Como era de costumbre entre los nobles mexicas, Moctezuma fue educado en el

Calmécac, la institución para la educación de los nobles mexicas. Según algunas fuentes, Moctezuma se destacó en su niñez durante su educación por su disciplina, cumpliendo sus trabajos adecuadamente y siendo devoto a la

religión mexica.​

</p>

<p>
Antes de volverse tlatoani o rey de México-Tenochtitlán, Moctezuma fue reconocido por su carrera militar. Él poseía el alto rango de

tlacochcalcatl (persona de la casa de los dardos) y/o tlacatecuhtli (señor de hombres) en el ejército mexica. Durante el reinado de Ahuízotl, entre fines del siglo XV y principios del XVI, participó en varias campañas reconocidas. Por ejemplo: reconocidamente participó en la conquista de Ayotlan, donde fue enviado por Ahuízotl para rescatar a comerciantes

pochtecas que estaban bajo ataque. Esta pelea en particular no duró mucho, ya que los de Ayotlan se rindieron poco después de la llegada de Moctezuma.​
</p>

<p>
Aproximadamente en 1490, Moctezuma alcanzó el rango de tequihua, el cual se obtiene al capturar al menos 4 comandantes enemigos.​
</p>

<p>
También se trató del sumo sacerdote del templo de

Huitzilopochtli. Fueron su carrera militar y su influencia religiosa como sacerdote los factores más relevantes en su elección como tlatoani.​
</p>

<h2>Coronación</h2>

<p>
Moctezuma fue elegido como el sucesor de Ahuízotl tras la muerte de éste causada por un fuerte golpe que sufrió durante una inundación en Tenochtitlán en 1502. La inundación, proveniente del sitio de Huitzilopochco (actualmente conocido como

Churubusco), causó gran destrucción en la ciudad, pues la gran ola azotó a varios edificios y ahogó a varias personas. El golpe ocurrió mientras Ahuízotl intentaba huir de la ola, golpeándose la cabeza con el

umbral de una puerta. La herida mató a Ahuízotl varios meses después de la inundación.​
</p>

<image-element float="left" src="/res/coronacion-de-moctezuma.png" alt="Celebración por la coronación de moctezuma"></image-element>

<p>
No se sabe con certidumbre la fecha de coronación de Moctezuma, ya que las fuentes ofrecen diversas fechas entre los años de 1502 y 1503. Una inscripción tallada en piedra actualmente ubicada en el

Instituto de Arte de Chicago conocida como la Piedra de los Cinco Soles o Piedra de la Coronación de Motecuhzoma II, por ejemplo, da la siguiente fecha del

calendario mexica: día 1 cocodrilo del año 11 carrizo. Esta fecha es equivalente al día 15 de julio de 1503 en el

calendario gregoriano, y algunos historiadores han sospechado que esta fecha se refiere a la coronación.​ Otra fecha en este mismo año es dada por

Fernando de Alva Ixtlilxóchitl, un importante historiador

mestizo en el

México colonial, siendo esta el 24 de mayo.​ El año más generalmente aceptado por historiadores actuales, sin embargo, es el año de 1502.​
</p>

<p>
Su ceremonia de coronación duró 4 días, durante los cuales realizó sus primeras reformas políticas, planeándolas junto a su tío Tlilpotonqui, hijo de

Tlacaélel y

Cihuacóatl de México.​ 
</p>

